// Refaça o código anterior e numero seja menor ou igual a 3 apresente o dobro. Caso o numero seja maior apresente o triplo
var n , r , t
n=parseInt(prompt("Digite um valor:"))
if(n<=3){
    r=n*2
    alert("Seu número é maenor ou igual a 3, o dobro dele é: "+r)
}
else{
    t=n*3
    alert("Seu número é maior que três. Portanto aqui esta o triplo: "+t)
}
    
